    public static make(s: string, font: Font): string {
        let builtString: string = s;
        s.split("").forEach((char, i) => {
            if (UniFont.NORMAL.includes(char)) {
                let id:number = UniFont.NORMAL.indexOf(char);
                if (font.length > id) {
                    builtString = builtString.replace(char, font[id]);
                }
            }
        });
        return builtString;
    }
